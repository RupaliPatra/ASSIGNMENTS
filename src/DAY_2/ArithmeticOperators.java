package DAY_2;

public class ArithmeticOperators {

	public static void main(String[] args) {
		 int a = 60, b = 12;

		    // addition operator
		    System.out.println("a + b = " + (a + b));

		    // subtraction operator
		    System.out.println("a - b = " + (a - b));

		    // multiplication operator
		    System.out.println("a * b = " + (a * b));

		    // division operator
		    System.out.println("a / b = " + (a / b));

		    // modulus operator
		    System.out.println("a % b = " + (a % b));
		  }
}
